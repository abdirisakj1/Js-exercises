let a = 15;
let b = "15";

// comparison operators

// equal to
console.log(a == b);

// not equal to
console.log(a != b);

// strictly to
console.log(a === b);

// strictly not equal
console.log(a !== b);
// greater then 
console.log(a > b);

// less then

console.log(a < b);

// greater then? or equal
console.log(a >= b);

// less then ? or equal
console.log(a <= b);
